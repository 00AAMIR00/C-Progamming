#include <stdio.h>

int main(void)
{
   // x++ , x-- // ITS MEANS USE THE VALUE THAN INCREMENT/DECREMENT
 // example
 int x = 6;
 printf("%d",x++);
 // no increment happend


   // ++x ,--x // ITS MEANS INCREMENT/DECREMENT THE VALUE THAN USE
   int y = 6;
  printf("%d",--y);
  // decrement happend
    return 0;
}
